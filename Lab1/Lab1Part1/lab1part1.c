#include <stdio.h>
int main() {
  printf("C uses escape sequences for a variety of purposes.\n");
  printf("Some common ones are:\n");
  printf("\t  to print \", use \\\"\n");
  printf("\t  to print \\, use \\\\\n");
  printf("\t  to go to a new line, use \\n\n");
  printf("     However to print %%, we use %%%%\n");
  return 0;
}